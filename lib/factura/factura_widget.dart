          f: widget.vmcprice,
          c: 'VMFINSUSAAA001',
          g: FFAppState().niv,
        );
        _model.soundPlayer ??= AudioPlayer();
        if (_model.soundPlayer!.playing) {
          await _model.soundPlayer!.stop();
        }
        _model.soundPlayer!.setVolume(0.72);
        _model.soundPlayer!
            .setAsset('assets/audios/Compra.mp3')
            .then((_) => _model.soundPlayer!.play());

        _model.psc = await PremioCall.call();
        if (PremioCall.lot(
          (_model.psc?.jsonBody ?? ''),
        )) {
          context.pushNamed(
            'RuletaHomeWin',
            queryParameters: {
              'premio': serializeParam(
                PremioCall.lot(
                  (_model.psc?.jsonBody ?? ''),
                ).toString().toString(),
                ParamType.String,
              ),
              'txtslot': serializeParam(
                'Reclamar Premio',
                ParamType.String,
              ),
              'slot': serializeParam(
                PremioCall.slot(
                  (_model.psc?.jsonBody ?? ''),
                ),
                ParamType.int,
              ),
            }.withoutNulls,
          );

          return;
        } else {
          context.pushNamed('RuletaHome');

          return;
        }
      } else {
        return;
      }
    });

    WidgetsBinding.instance.addPostFrameCallback((_) => setState(() {}));
  }

